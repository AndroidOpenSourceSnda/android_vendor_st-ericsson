                    {
                        p2_cplx->p_next      = p1_cplx_next;
                        p2_cplx_prev->p_next = p1_cplx;
                    }
                    p1_cplx->p_next          = p2_cplx_next;
                    p_cplx_swap              = p1_cplx;
                    p1_cplx                  = p2_cplx;
                    p2_cplx                  = p_cplx_swap;
                    p1_cplx_next             = p1_cplx->p_next;
                    p2_cplx_next             = p2_cplx->p_next;
